    def forward(self, inputs):
    
        if inputs.shape[0] > self.part:
            # nn.Linear performs randomly when batch size is too large
            num_parts = inputs.shape[0] // self.part
            part_linear_out = [self.linear(inputs[num_part*self.part:(num_part+1)*self.part])
                               for num_part in range(num_parts+1)]
            x = torch.cat(part_linear_out, dim=0)
        else:
            x = self.linear(inputs)
        torch.backends.cudnn.enabled = False
        x = self.norm(x.permute(0, 2, 1)).permute(0, 2, 1) if self.use_norm else x
        torch.backends.cudnn.enabled = True
        x = F.relu(x)
        
        ca_max = torch.max(x, dim=1, keepdim=True)[0]
        ca_mean = torch.mean(x, dim=1, keepdim=True)
        ca_max = self.ca_mlp(ca_max)
        ca_mean = self.ca_mlp(ca_mean)
        ca_score = F.sigmoid(ca_mean + ca_max)

        ca_x = x * ca_score
        ca_x = torch.mean(ca_x, dim=1, keepdim=False)
        
        sa_max = torch.max(x, dim=2, keepdim=True)[0]
        sa_mean = torch.mean(x, dim=2, keepdim=True)
        sa_x = torch.cat((sa_max, sa_mean), dim=2)
        sa_score = F.softmax(self.sa_mlp(sa_x), dim=1)
        
        sa_x = x * sa_score
        sa_x = torch.sum(sa_x, dim=1)
        
        pap_x = sa_x + ca_x

        if self.last_vfe:
            return pap_x
        else:
            x_repeat = pap_x.repeat(1, inputs.shape[1], 1)
            x_concatenated = torch.cat([x, x_repeat], dim=2)
            return x_concatenated

