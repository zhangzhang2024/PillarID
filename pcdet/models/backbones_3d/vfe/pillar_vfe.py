class PSA(nn.Module):
    def __init__(self,
                 in_channels,
                 out_channels,
                 use_norm=True,
                 last_layer=False):
        super().__init__()
        
        self.last_vfe = last_layer
        self.use_norm = use_norm
        if not self.last_vfe:
            out_channels = out_channels // 2

        if self.use_norm:
            self.linear = nn.Linear(in_channels, out_channels, bias=False)
            self.norm = nn.BatchNorm1d(out_channels, eps=1e-3, momentum=0.01)
        else:
            self.linear = nn.Linear(in_channels, out_channels, bias=True)
        self.token_mixer = nn.Linear(2, 1, bias=False)
        self.layernorm = nn.LayerNorm(out_channels)
        self.channel_mlp = nn.Linear(out_channels, out_channels, bias=False)
        self.part = 50000

    def forward(self, inputs):
    
        if inputs.shape[0] > self.part:
            # nn.Linear performs randomly when batch size is too large
            num_parts = inputs.shape[0] // self.part
            part_linear_out = [self.linear(inputs[num_part*self.part:(num_part+1)*self.part])
                               for num_part in range(num_parts+1)]
            x = torch.cat(part_linear_out, dim=0)
        else:
            x = self.linear(inputs)
        torch.backends.cudnn.enabled = False
        x = self.norm(x.permute(0, 2, 1)).permute(0, 2, 1) if self.use_norm else x
        torch.backends.cudnn.enabled = True
        
        layernorm_x = self.layernorm(x)
        psa_max = torch.max(layernorm_x, dim=2, keepdim=True)[0]
        psa_mean = torch.mean(layernorm_x, dim=2, keepdim=True)
        psa_x = torch.cat((psa_max, psa_mean), dim=2)
        psa_score = F.softmax(self.token_mixer(psa_x), dim=1)
        
        psa_x = layernorm_x * psa_score
        psa_x = torch.sum(psa_x, dim=1) + torch.mean(x, dim=1, keepdim=False)
        psa_x = self.channel_mlp(self.layernorm(psa_x)) + psa_x

        if self.last_vfe:
            return psa_x
        else:
            x_repeat = psa_x.repeat(1, inputs.shape[1], 1)
            x_concatenated = torch.cat([x, x_repeat], dim=2)
            return x_concatenated

